helloworld = "helloworld2"
# 赋值
# "helloworld2" 赋值给 helloworld变量
# 在Python中，变量可以不用事先声明再调用

print("helloworld1")
#打印字符串helloworld1
# print()函数可以进行打印 打印内容包括字符串 变量
# 用两个双（单）引号括起来的文字我们称为字符串

print(helloworld)
# helloworld为“helloworld2”的变量名
# #打印名为helloworld变量名的值（helloworld变量名的值为“helloworld”）
# 此处的helloworld为变量 其所拥有的值为“helloworld2”  即字符串

print('helloworld3')
# 打印字符串helloworld3
# 单双引号相同 都表示的是字符串
