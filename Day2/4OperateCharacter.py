"""
运算符:
在编程语言中 运算符一般分为单目运算符和双目运算符
单目运算符
其中单目运算符中 +加-减*乘/除并不少见 其中比较特殊的便是%求余
求余可以简单的理解为5/2=2···1
即5除2得2余1 /除运算所得的结果即为2 %运算所得的余数即为余数1
双目运算符
双目运算符中 常见的运算符为++即自增运算符 --自减运算符
此外 还有另外比较特殊的用于比较的比较运算符
== 用于比较两个数字（或字符串等）是否等价
！= 用于取非运算 即不成立 与not保留字有相同的作用
"""
a = 1 # 此处的单个等号为赋值 意为将1赋值给变量a
b = 2 # 此处的单个等号为赋值 意为将2赋值给变量b

if a == b:
    # 此处的双等号代表判断 判断a、b是否相等
    # 若相等 则进入下面的语句中 若不相等 则进入elif语句中
    print("a b相等")
elif a != b:
    # 此处的！=也代表判断 与==不同 ！=代表的为不等于
    # 此处意为若a！=b 则进入下方的语句中
    print("a b不相等")